const currentPage = document.body.dataset.page || 'home';
const savedLang = localStorage.getItem('aleliLang') || 'es';

function applyLanguage(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.textContent = translations[lang][key];
    }
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.dataset.i18nAria;
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.setAttribute('aria-label', translations[lang][key]);
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.setAttribute('placeholder', translations[lang][key]);
    }
  });
  document.querySelectorAll('.lang-toggle').forEach((btn) => {
    btn.textContent = lang === 'es' ? 'EN' : 'ES';
  });
  localStorage.setItem('aleliLang', lang);
}

function setupLanguageToggle() {
  document.querySelectorAll('.lang-toggle').forEach((btn) => {
    btn.addEventListener('click', () => {
      const nextLang = (localStorage.getItem('aleliLang') || 'es') === 'es' ? 'en' : 'es';
      applyLanguage(nextLang);
    });
  });
}

function setupMenu() {
  const menuBtn = document.getElementById('menuBtn');
  const closeBtn = document.getElementById('closeMenu');
  const overlay = document.getElementById('mobileOverlay');

  const openMenu = () => {
    document.body.classList.add('menu-open');
    overlay?.classList.remove('hidden');
  };

  const closeMenu = () => {
    document.body.classList.remove('menu-open');
    overlay?.classList.add('hidden');
  };

  if (menuBtn) menuBtn.addEventListener('click', openMenu);
  if (closeBtn) closeBtn.addEventListener('click', closeMenu);
  if (overlay) overlay.addEventListener('click', closeMenu);

  document.querySelectorAll('.nav-link').forEach((link) => {
    link.addEventListener('click', () => {
      closeMenu();
    });
  });

  window.addEventListener('resize', () => {
    if (window.innerWidth > 860) {
      closeMenu();
    }
  });
}

function setupPopup() {
  const popup = document.getElementById('entryPopup');
  const closeBtn = document.getElementById('popupClose');
  if (!popup || !closeBtn) return;
  const seen = sessionStorage.getItem('aleliPopupSeen');
  if (!seen) {
    popup.classList.remove('hidden');
    document.body.classList.add('popup-open');
  }
  closeBtn.addEventListener('click', () => {
    popup.classList.add('hidden');
    document.body.classList.remove('popup-open');
    sessionStorage.setItem('aleliPopupSeen', 'true');
  });
}

function markActivePage() {
  document.querySelectorAll('.nav-link[data-page]').forEach((link) => {
    if (link.dataset.page === currentPage) link.classList.add('active');
  });
}

function setupLightbox() {
  const lightbox = document.getElementById('lightbox');
  if (!lightbox) return;
  const lightboxImg = document.getElementById('lightboxImage');
  const lightboxCaption = document.getElementById('lightboxCaption');
  const close = document.getElementById('lightboxClose');

  document.querySelectorAll('[data-lightbox-src]').forEach((img) => {
    img.addEventListener('click', () => {
      lightboxImg.src = img.dataset.lightboxSrc || img.src;
      lightboxImg.alt = img.alt || '';
      lightboxCaption.textContent = img.dataset.caption || '';
      lightbox.classList.remove('hidden');
      document.body.classList.add('popup-open');
    });
  });

  const hide = () => {
    lightbox.classList.add('hidden');
    document.body.classList.remove('popup-open');
  };
  close?.addEventListener('click', hide);
  lightbox.addEventListener('click', (e) => {
    if (e.target === lightbox) hide();
  });
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') hide();
  });
}

function setupFilters() {
  const chips = document.querySelectorAll('.filter-chip');
  const cards = document.querySelectorAll('.gallery-card[data-category]');
  if (!chips.length || !cards.length) return;
  chips.forEach(chip => {
    chip.addEventListener('click', () => {
      chips.forEach(c => c.classList.remove('active'));
      chip.classList.add('active');
      const filter = chip.dataset.filter;
      cards.forEach(card => {
        const match = filter === 'all' || card.dataset.category.includes(filter);
        card.style.display = match ? '' : 'none';
      });
    });
  });
}

applyLanguage(savedLang);
window.addEventListener('DOMContentLoaded', () => {
  setupLanguageToggle();
  setupMenu();
  setupPopup();
  setupLightbox();
  setupFilters();
  markActivePage();
});
